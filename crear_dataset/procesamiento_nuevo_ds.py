"""##Para el renombramiento de las secuencias del archivo multi FASTA basándose en la información del archivo Excel y generar un archivo con la relación de nombres antiguos y nuevos"""

import pandas as pd
from Bio import SeqIO

# 📌 Mapeo de etiquetas a prefijos
tipo_etiqueta = {
    "HydroArc": "Ha",
    "TransArc": "Ta",
    "HydroBac": "Hb",
    "TransBac": "Tb",
    "TranseHydroBac": "THb",
    "TranseHydroArc": "THa",
    "HydroEu": "He",
    "TransEu": "Te",
    "TranseHydroEu": "THe"
}

def renombrar_secuencias(input_fasta, excel_file, output_fasta, output_relacion):
    """
    Renombra las secuencias de un archivo FASTA basado en un archivo Excel y las etiqueta con un formato específico.
    También genera un archivo con la relación entre nombres originales y nuevos.
    """
    # 📥 Leer el archivo Excel (Hoja 3)
    df = pd.read_excel(excel_file, sheet_name=2)  # Hoja 3 (índice 2 porque es 0-based)

    # Convertir la columna ID_especie en índice para búsqueda rápida
    id_to_tipo = pd.Series(df["Tipo"].values, index=df["ID_Especie"]).to_dict()

    # Diccionario para rastrear cuántas secuencias se han renombrado por etiqueta
    contador_etiquetas = {clave: 0 for clave in tipo_etiqueta.values()}

    # 📤 Preparar archivos de salida
    nueva_lista = []
    relacion_nombres = []

    # 🔄 Procesar secuencias
    with open(input_fasta, "r") as infile, open(output_fasta, "w") as outfile:
        for record in SeqIO.parse(infile, "fasta"):
            original_id = record.id

            # Verificar si el nombre original está en el Excel
            if original_id in id_to_tipo:
                tipo = id_to_tipo[original_id]

                # Verificar si el tipo tiene un mapeo definido
                if tipo in tipo_etiqueta:
                    etiqueta = tipo_etiqueta[tipo]
                    contador_etiquetas[etiqueta] += 1
                    nuevo_id = f"{etiqueta}{contador_etiquetas[etiqueta]}"
                else:
                    nuevo_id = f"Desconocido_{original_id}"
            else:
                nuevo_id = f"SinEtiqueta_{original_id}"

            # Actualizar el registro con el nuevo ID
            record.id = nuevo_id
            record.description = ""  # Eliminamos descripciones adicionales para limpieza
            nueva_lista.append(record)

            # Guardar relación de nombres
            relacion_nombres.append(f"{original_id}\t{nuevo_id}\n")

        # Guardar el nuevo archivo FASTA
        SeqIO.write(nueva_lista, outfile, "fasta")

    # Guardar la relación de nombres en un archivo de texto
    with open(output_relacion, "w") as rel_file:
        rel_file.writelines(relacion_nombres)

    print(f"✅ Archivo renombrado guardado en: {output_fasta}")
    print(f"📜 Relación de nombres guardada en: {output_relacion}")

