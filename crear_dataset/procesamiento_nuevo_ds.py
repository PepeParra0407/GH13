"""📌 ¿Qué hace el siguiente código?

✅ Compara dos archivos multifasta:

fasta_A: Archivo con 528 secuencias
fasta_B: Archivo con 645 secuencias

✅ Elimina secuencias en fasta_A que no estén en fasta_B

✅ Agrega secuencias nuevas de fasta_B que no están en fasta_A

✅ Elimina duplicados basándose en la secuencia

✅ Guarda el resultado en un nuevo archivo

✅ Cuenta cuántas secuencias se eliminaron, agregaron y cuántas eran duplicadas
"""

from Bio import SeqIO

def comparar_fasta(fasta_A, fasta_B, output_fasta):
    """
    Compara dos archivos multifasta:
    - Elimina secuencias en fasta_A que no están en fasta_B.
    - Agrega secuencias nuevas de fasta_B que no están en fasta_A.
    - Elimina secuencias duplicadas.
    - Guarda el resultado en output_fasta.
    - Reporta estadísticas de cambios realizados.
    """

    # 📥 Leer secuencias de ambos archivos
    secuencias_A = {str(record.seq): record for record in SeqIO.parse(fasta_A, "fasta")}
    secuencias_B = {str(record.seq): record for record in SeqIO.parse(fasta_B, "fasta")}

    # 🔍 Determinar qué secuencias eliminar (presentes en A pero no en B)
    eliminadas = {seq for seq in secuencias_A if seq not in secuencias_B}

    # 🔄 Determinar qué secuencias agregar (presentes en B pero no en A)
    agregadas = {seq for seq in secuencias_B if seq not in secuencias_A}

    # 🛠 Construir el conjunto final de secuencias únicas
    secuencias_finales = {seq: secuencias_B[seq] for seq in secuencias_B}  # Comenzar con B
    secuencias_finales.update({seq: secuencias_A[seq] for seq in secuencias_A if seq in secuencias_B})  # Mantener A si está en B

    # 📊 Reporte de estadísticas
    num_original_A = len(secuencias_A)
    num_original_B = len(secuencias_B)
    num_final = len(secuencias_finales)

    print(f"📂 Archivo A: {num_original_A} secuencias")
    print(f"📂 Archivo B: {num_original_B} secuencias")
    print(f"❌ Secuencias eliminadas: {len(eliminadas)}")
    print(f"➕ Secuencias agregadas: {len(agregadas)}")
    print(f"🔄 Secuencias repetidas eliminadas: {(num_original_A + num_original_B) - num_final}")
    print(f"✅ Secuencias finales en el nuevo archivo: {num_final}")

    # 📤 Guardar resultado en un nuevo archivo multifasta
    with open(output_fasta, "w") as outfile:
        SeqIO.write(secuencias_finales.values(), outfile, "fasta")

    print(f"✅ Archivo final guardado en: {output_fasta}")

