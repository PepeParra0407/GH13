import numpy as np
import os
import h5py
import torch
from Bio import SeqIO
from tqdm import tqdm
from esm.sdk.api import ESMProtein
from esm.models.esmc import ESMC, LogitsConfig


# ===============================
#   CONFIGURACIÓN DEL MODELO
# ===============================
device = "cuda" if torch.cuda.is_available() else "cpu"

EMBEDDING_CONFIG = LogitsConfig(
    sequence=True,
    return_embeddings=True,
    return_hidden_states=True
)

model = ESMC.from_pretrained("esmc_300m").to(device)
model.eval()

# Initialize tokenizer here
tokenizer = model.tokenizer


# ===============================
#   FUNCIÓN: OBTENER EMBEDDING
# ===============================
def get_hs18_embedding(sequence: str):
    """
    Extrae un embedding a nivel de secuencia de la capa 18 del modelo ESM3.
    Devuelve un tensor CPU listo para guardar (shape: 1 x 960).
    """
    tokens = tokenizer(sequence, return_tensors="pt", truncation=True, padding=True)
    input_ids = tokens["input_ids"].to(device)

    with torch.no_grad():
        output = model.forward(input_ids)

    hidden_states = output.hidden_states  # (layers, L, dim)
    hidden_states = hidden_states[0] if isinstance(hidden_states, tuple) else hidden_states
    hs18 = hidden_states[18]              # capa 18 → (L, dim)

    # Realizar mean pooling para obtener un embedding a nivel de secuencia
    sequence_embedding = torch.mean(hs18, dim=0) # (dim,)

    # Asegurarse de que el embedding tenga la forma (1, dim) para consistencia
    return sequence_embedding.cpu().unsqueeze(0) # (1, dim)

# ===============================
#   PROCESAR TODOS LOS FASTA
# ===============================
input_dir = "input" # Corrected to be a directory name
output_h5 = "embeddings_esmc_hs18_sequence_fm_mea.h5" # Changed output H5 file to combine all ESM embeddings

# Create the directory if it doesn't exist
os.makedirs(input_dir, exist_ok=True)

fasta_files = [f for f in os.listdir(input_dir) if f.endswith(".fasta") or f.endswith(".fa")]

if len(fasta_files) == 0:
    raise ValueError("No se encontraron archivos FASTA en fasta_inputs/")

print(f"Archivos FASTA encontrados: {fasta_files}")
print(f"Guardando embeddings en: {output_h5}")


with h5py.File(output_h5, "w") as h5out:

    for fasta_name in fasta_files:
        fasta_path = os.path.join(input_dir, fasta_name)

        print(f"\nProcesando {fasta_name}...\n")

        for record in tqdm(SeqIO.parse(fasta_path, "fasta")):

            seq_id = record.id          # nombre original
            seq = str(record.seq)

            # Obtener embedding
            embedding = get_hs18_embedding(seq)  # tensor (1, dim)

            # Convert to float32 before converting to numpy
            h5out.create_dataset(
                seq_id,
                data=embedding.float().numpy(),
                compression="gzip"
            )


print("\n🚀 Listo: Todos los embeddings fueron guardados en", output_h5)

