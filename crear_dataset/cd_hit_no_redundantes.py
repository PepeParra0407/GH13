"""##Código para contar secuencias por etiqueta en el archivo filtrado"""

from collections import Counter

# Definir etiquetas de interés
etiquetas = ["Ha", "Hb", "Ta", "Tb", "Te", "He", "HTa", "HTb", "HTe", "THa", "THb", "THe"]
conteo_etiquetas = Counter()

file_name = "/content/DS_362_only_H_nr_80"

# Leer el archivo y contar las etiquetas en los identificadores
with open(file_name, "r") as fasta_file:
    for line in fasta_file:
        if line.startswith(">"):  # Identificadores comienzan con ">"
            for etiqueta in etiquetas:
                if etiqueta in line:
                    conteo_etiquetas[etiqueta] += 1
                    break  # Evita contar más de una etiqueta por línea

# Mostrar resultados
for etiqueta, count in conteo_etiquetas.items():
    print(f"{etiqueta}: {count} secuencias")

