from Bio import SeqIO

def extraer_nombres_fasta(input_fasta, output_txt):
    """
    Extrae los nombres (IDs) de las secuencias de un archivo FASTA
    y los guarda en un archivo de texto.
    """
    with open(output_txt, "w") as outfile:
        for record in SeqIO.parse(input_fasta, "fasta"):
            outfile.write(f"{record.id}\n")

    print(f"✅ Lista de nombres guardada en: {output_txt}")

