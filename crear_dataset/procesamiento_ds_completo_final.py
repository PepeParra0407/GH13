import pandas as pd
from Bio import SeqIO

# --- CONFIGURACIÓN ---
nombre_excel = "GH13_HT_renombradas.xlsx"
hoja_excel = "Renombre_BAEHT"
col_id = "ID Uniprot"
col_nuevo_nombre = "Nuevo_nombre_lista"
archivo_fasta = "DS_BAETH_completo(735)_solo_IDs"  # Cambia esto por tu archivo real

# Leer el Excel y crear el diccionario de renombramiento
df = pd.read_excel(nombre_excel, sheet_name=hoja_excel)
rename_dict = dict(zip(df[col_id], df[col_nuevo_nombre]))

# Leer y renombrar las secuencias del multifasta
secuencias_renombradas = []
renombrados = []
no_encontrados = []

for record in SeqIO.parse(archivo_fasta, "fasta"):
    original_id = record.id
    if original_id in rename_dict:
        nuevo_id = rename_dict[original_id]
        record.id = nuevo_id
        record.description = ""  # Limpia la descripción para evitar texto duplicado
        renombrados.append((original_id, nuevo_id))
    else:
        no_encontrados.append(original_id)
    secuencias_renombradas.append(record)

# Guardar el nuevo multifasta renombrado
archivo_salida = archivo_fasta.replace(".fasta", "_renombrado.fasta")
SeqIO.write(secuencias_renombradas, archivo_salida, "fasta")

# Guardar el log de cambios
with open("log_cambios_renombre.txt", "w") as f:
    f.write("Renombrados:\n")
    for original, nuevo in renombrados:
        f.write(f"{original} -> {nuevo}\n")
    f.write("\nNo encontrados:\n")
    for nf in no_encontrados:
        f.write(f"{nf}\n")

# Mostrar resumen
print(f"Secuencias procesadas: {len(secuencias_renombradas)}")
print(f"Renombradas: {len(renombrados)}")
print(f"No encontradas: {len(no_encontrados)}")
print(f"Archivo renombrado: {archivo_salida}")
print("Log guardado en: log_cambios_renombre.txt")