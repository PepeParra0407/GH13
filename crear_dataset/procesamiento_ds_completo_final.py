from Bio import SeqIO

def contar_secuencias(fasta_file):
    """Cuenta cuántas secuencias hay en un archivo multifasta."""
    num_secuencias = sum(1 for _ in SeqIO.parse(fasta_file, "fasta"))
    print(f"📊 El archivo '{fasta_file}' contiene {num_secuencias} secuencias.")
    return num_secuencias

