from google.colab import files
from collections import defaultdict, Counter

# Paso 1: Subir archivo
uploaded = files.upload()

input_file = list(uploaded.keys())[0]
output_file = "g15_p8_16-05-2025_ordenado.txt"

# Leer líneas del archivo
with open(input_file, "r") as f:
    lines = f.readlines()

# Agrupar líneas por posición (tercera columna(>= 4:))
groups = defaultdict(list)
for line in lines:
    cols = line.strip().split()
    if len(cols) >= 4:
        try:
            group_key = int(cols[1])
            groups[group_key].append(line)
        except ValueError:
            pass  # Ignorar líneas sin posición válida

# Ordenar cada grupo por p_value (cuarta columna)
def get_pvalue(line):
    cols = line.strip().split()
    try:
        return float(cols[4])  # columna 5 → índice 4
    except (IndexError, ValueError):
        return float('inf')

# Crear lista final ordenada
sorted_lines = []
for pos in sorted(groups.keys()):  # Ordenar grupos por posición ascendente
    group_lines = groups[pos]
    group_sorted = sorted(group_lines, key=get_pvalue)
    sorted_lines.extend(group_sorted)

# Guardar resultado
with open(output_file, "w") as f_out:
    f_out.writelines(sorted_lines)

print(f"Archivo ordenado y agrupado guardado como: {output_file}")

# --- Parte extra: extraer valores de la tercera columna ---

third_col_values = []
for line in sorted_lines:
    cols = line.strip().split()
    if len(cols) >= 4:
        try:
            val = int(cols[3])
            third_col_values.append(val)
        except ValueError:
            pass

# Contar frecuencias con Counter
counter = Counter(third_col_values)

# Crear salida de valores únicos separados por comas (ordenados numéricamente)
unique_sorted_values = sorted(counter.keys())
output_string = ",".join(str(v) for v in unique_sorted_values)
print("\nValores únicos ordenados de la tercera columna (separados por comas):")
print(output_string)

# Ordenar por frecuencia (de mayor a menor)
freq_sorted = sorted(counter.items(), key=lambda x: x[1], reverse=True)

# Imprimir frecuencias ordenadas por cantidad de apariciones
print("\nFrecuencia de aparición de cada número en la tercera columna (ordenado por veces de aparición):")
for num, count in freq_sorted:
    print(f"{num}: {count} veces")

