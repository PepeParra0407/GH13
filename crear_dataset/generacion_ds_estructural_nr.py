import zipfile
import os
import shutil
import tempfile

def filtrar_archivos_zip_por_ids(zip_path, ids_txt_path, salida_zip_filtrado):
    # Leer los IDs desde el archivo de texto
    with open(ids_txt_path, 'r') as f:
        ids_deseados = set(line.strip() for line in f if line.strip())

    encontrados = set()
    no_encontrados = set(ids_deseados)

    with zipfile.ZipFile(zip_path, 'r') as zip_origen:
        archivos_en_zip = zip_origen.namelist()

        # Crear una carpeta temporal para extraer los archivos deseados
        with tempfile.TemporaryDirectory() as tempdir:
            archivos_a_incluir = []

            for archivo in archivos_en_zip:
                # Solo se toman archivos con extensión .pdb
                if archivo.endswith('.pdb'):
                    nombre_base = os.path.splitext(os.path.basename(archivo))[0]
                    if nombre_base in ids_deseados:
                        encontrados.add(nombre_base)
                        no_encontrados.discard(nombre_base)
                        ruta_destino = os.path.join(tempdir, os.path.basename(archivo))
                        with open(ruta_destino, 'wb') as f_out:
                            f_out.write(zip_origen.read(archivo))
                        archivos_a_incluir.append(ruta_destino)

            # Crear un nuevo archivo ZIP solo con los archivos encontrados
            with zipfile.ZipFile(salida_zip_filtrado, 'w') as zip_nuevo:
                for archivo in archivos_a_incluir:
                    zip_nuevo.write(archivo, arcname=os.path.basename(archivo))

    # Guardar reporte de encontrados y no encontrados
    with open("encontrados.txt", 'w') as f:
        f.write('\n'.join(sorted(encontrados)))
    with open("no_encontrados.txt", 'w') as f:
        f.write('\n'.join(sorted(no_encontrados)))

    print(f"✅ Archivos encontrados ({len(encontrados)}): {sorted(encontrados)}")
    print(f"❌ Archivos no encontrados ({len(no_encontrados)}): {sorted(no_encontrados)}")
    print(f"📦 Nuevo zip creado: {salida_zip_filtrado}")

# Uso del script
filtrar_archivos_zip_por_ids(
    zip_path="/content/estructuras_renombradas(702)_PDB.zip",  # Archivo .zip con los archivos PDB
    ids_txt_path="/content/DS_362_nr_80_merge_etiquetas.txt",     # Archivo .txt con los IDs
    salida_zip_filtrado="DS_estructural_362_nr80_merge.zip"  # Nuevo archivo zip con los archivos encontrados
)