def extraer_nombres_fasta(fasta_file, output_file):
    with open(fasta_file, 'r', encoding='utf-8') as f, open(output_file, 'w', encoding='utf-8') as out:
        for line in f:
            if line.startswith('>'):
                out.write(line[1:].strip() + '\n')
    print(f"Nombres de secuencias guardados en '{output_file}'")

