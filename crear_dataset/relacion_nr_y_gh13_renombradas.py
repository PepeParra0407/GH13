import pandas as pd

def buscar_identificadores(txt_file, excel_file, sheet_name, col_busqueda, cols_extras, output_file):
    # Leer los identificadores desde el archivo de texto
    with open(txt_file, 'r', encoding='utf-8') as f:
        identificadores = set(line.strip() for line in f if line.strip())

    print(f"Total de identificadores en el archivo de texto: {len(identificadores)}")

    # Leer la base de datos desde la hoja específica de Excel
    df = pd.read_excel(excel_file, sheet_name=sheet_name, dtype=str)

    # Limpiar espacios extra en la columna de búsqueda
    df[col_busqueda] = df[col_busqueda].astype(str).str.strip()

    # Filtrar las filas donde la columna de búsqueda contenga un identificador
    df_filtrado = df[df[col_busqueda].isin(identificadores)]

    print(f"Total de coincidencias encontradas: {df_filtrado.shape[0]}")

    if df_filtrado.empty:
        print("No se encontraron coincidencias.")
        return

    # Seleccionar las columnas requeridas
    df_resultado = df_filtrado[[col_busqueda] + cols_extras]

    # Guardar resultados en un archivo de texto separado por tabulaciones
    df_resultado.to_csv(output_file, sep='\t', index=False, header=True, encoding='utf-8')
    print(f"Búsqueda completada. Resultados guardados en '{output_file}'")

