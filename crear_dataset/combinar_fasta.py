output_txt = "DS_362_nr_80_merge_etiquetas.txt"

# Extraer etiquetas
with open(output_txt, "w") as out_file:
    for record in SeqIO.parse(fasta_file, "fasta"):
        out_file.write(record.id + "\n")

print(f"Etiquetas guardadas en '{output_txt}'")