from Bio import SeqIO

# Archivos que quieres combinar
input_files = ["DS_362_only_H_nr_80", "DS_362_only_T_nr_80"]
output_file = "DS_362_nr_80_merge"

# Lista para almacenar todas las secuencias
all_sequences = []

# Leer todas las secuencias de cada archivo
for file in input_files:
    records = list(SeqIO.parse(file, "fasta"))
    all_sequences.extend(records)

# Escribir el archivo combinado
SeqIO.write(all_sequences, output_file, "fasta")

print(f"Archivo combinado guardado como: {output_file}")
print(f"Total de secuencias combinadas: {len(all_sequences)}")

fasta_file = "/content/DS_nr_80_merge"
etiquetas = []

# Leer y extraer los IDs
for record in SeqIO.parse(fasta_file, "fasta"):
    etiquetas.append(record.id)

# Mostrar cuántas etiquetas hay
print(f"Total de etiquetas encontradas: {len(etiquetas)}")

# Mostrar las primeras 10 etiquetas como ejemplo
print("Etiquetas:")
print(etiquetas)

# Archivo FASTA de entrada
